inline uint32_t change_part(uint32_t q, uint32_t op, uint32_t s, int n)
{
	return q | (op & (n << s));
}
inline uint8_t read_part(uint32_t q, uint32_t op, uint32_t s)
{
	return (q & op) >> s;
}

